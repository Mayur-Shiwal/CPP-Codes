class Solution {
  public:
    int setBits(int N) {
        // Write Your Code here
        string s = bitset<32>(N).to_string();
        int ans = 0;
        for(auto it: s){
            if(it == '1') ans++;
        }
        return ans;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int N;
        cin >> N;

        Solution ob;
        int cnt = ob.setBits(N);
        cout << cnt << endl;
    }
    return 0;
}

